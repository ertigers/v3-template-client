/**
 * @description 处理 prop 为多级嵌套的情况，返回的数据 (列如: prop: user.name)
 * @param {Object} row 当前行数据
 * @param {String} prop 当前 prop
 * @returns {*}
 * */
export function handleRowAccordingToProp(row, prop) {
  if (!prop.includes(".")) return row[prop] ?? "--";
  prop.split(".").forEach(item => (row = row[item] ?? "--"));
  return row;
}

/**
 * @description 处理 prop，当 prop 为多级嵌套时 ==> 返回最后一级 prop
 * @param {String} prop 当前 prop
 * @returns {String}
 * */
export function handleProp(prop) {
  const propArr = prop.split(".");
  if (propArr.length === 1) return prop;
  return propArr[propArr.length - 1];
}

/**
 * @description 根据枚举列表查询当需要的数据（如果指定了 label 和 value 的 key值，会自动识别格式化）
 * @param {String} callValue 当前单元格值
 * @param {Array} enumData 字典列表
 * @param {Array} fieldNames label && value && children 的 key 值
 * @param {String} type 过滤类型（目前只有 tag）
 * @returns {String}
 * */
export function filterEnum(callValue, enumData, fieldNames, type = "tag") {
  const value = fieldNames?.value ?? "value";
  const label = fieldNames?.label ?? "label";
  const children = fieldNames?.children ?? "children";
  let filterData = {};
  // 判断 enumData 是否为数组
  if (Array.isArray(enumData)) filterData = findItemNested(enumData, callValue, value, children);
  // 判断是否输出的结果为 tag 类型
  if (type == "tag") {
    return filterData?.tagType ? filterData.tagType : "";
  } else {
    return filterData ? filterData[label] : "--";
  }
}

/**
 * @description 递归查找 callValue 对应的 enum 值
 * */
export function findItemNested(enumData, callValue, value, children) {
  return enumData.reduce((accumulator, current) => {
    if (accumulator) return accumulator;
    if (current[value] === callValue) return current;
    if (current[children]) return findItemNested(current[children], callValue, value, children);
  }, null);
}

/**
 * @description 查询是否有指定权限
 * @param {Array} userPermissions 用户权限集
 * @param {Object} route 路由对象
 * @returns {Boolean} 是否有权限
 * */
export function hasPermission(userPermissions, route) {
  if (route.meta && route.meta.permissions) {
    // 检查用户是否拥有路由所需的任意一个权限
    return route.meta.permissions.some(permission => userPermissions.includes(permission));
  } else {
    // 没有权限要求的路由默认允许访问
    return true;
  }
}

/**
 * @description 查询是否有指定的所有权限
 * @param {Array} userPermissions 用户权限集
 * @param {Array} requiredPermissions 所需权限集
 * @returns {Boolean} 是否拥有所有权限
 * */
export function hasAllPermissions(userPermissions, requiredPermissions) {
  if (!requiredPermissions || requiredPermissions.length === 0) {
    return true;
  }
  return requiredPermissions.every(permission => userPermissions.includes(permission));
}

/**
 * @description 查询是否有指定权限中的任意一个
 * @param {Array} userPermissions 用户权限集
 * @param {Array} requiredPermissions 所需权限集
 * @returns {Boolean} 是否拥有任意一个权限
 * */
export function hasAnyPermission(userPermissions, requiredPermissions) {
  if (!requiredPermissions || requiredPermissions.length === 0) {
    return true;
  }
  return requiredPermissions.some(permission => userPermissions.includes(permission));
}

/**
 * @description 递归查找有权限的路由
 * @param {Array} routes 路由列表
 * @param {Array} userPermissions 用户权限集
 * @returns {Array} 过滤后的路由列表
 * */
export function filterAsyncRoutes(routes, userPermissions) {
  const res = [];
  routes.forEach(route => {
    const tmp = { ...route };
    if (hasPermission(userPermissions, tmp)) {
      if (tmp.children) {
        tmp.children = filterAsyncRoutes(tmp.children, userPermissions);
      }
      res.push(tmp);
    }
  });
  return res;
}

